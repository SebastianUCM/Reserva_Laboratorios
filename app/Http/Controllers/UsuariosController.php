     */
    public function store(Request $request)
    {
        
        $usuario = new User();
        $usuario->Nombre =request('Nombre');
        $usuario->email =request('email');
        $usuario->password = request('password');
        $usuario->rol = request('rol');
        $usuario->save();
        return redirect('/Usuarios');
    }

